								<h3>History</h3>
								<p>The disintegration of the well spread Gupta empire marks the onset of Rajputana, a collection of several princely states of Rajasthan. The Rajputs faced sever Muslim attacks but their courage and honour for their land kept them strong. This period of evolution witnessed great leaders like Prithvi Raj Chauhan, Rana Pratap of Mewar who defied the mighty Mughal Emperor Akbar.
However, in the 18th century, the Mughal Empire faced a major fall-back as the British made their presence felt in the country. Most of the princely states continued with self-governance but at the time of Independence, Rajputana majorly comprised of 18 princely states, two chieftains and a British administered province of Ajmer-Merwara.
The 1950s mark the merging of these 18 provinces into one whole state and Sawai Man Singh II, then Maharaja of Jaipur was made the Rajpramukh. Today Rajasthan has 33 districts covering an area of 3,42,239 sq km.</p>
