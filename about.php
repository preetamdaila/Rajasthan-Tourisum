								<h3>Topography</h3>
								<p>The contour of Rajasthan is varied and well divided by the Aravalli Range that spreads across the state from the southwest to the northeast. With an unproductive region in the northwest to a comparatively fertile land in the east. Rajasthan's topography is a potpourri of the sandy deserts and prolific vegetation. It is home to the Great Indian Thar Desert and the Chambal River which is solely responsible for the water supply in the region.</p>
							</div>
							<div class="6u 12u(mobilep)">
								<h3>Infrastructure</h3>
								<p>Irrespective of rich cultural roots, Rajasthan is equally modern and flourishing as any other urban city. The state roads flaunt the most expensive vehicles and at the same time run camel carts, horse drawn tongas and scooter rickshaws. The region is well connected by a network of roads and a steadily improving transport system.
When it comes to accommodation, Rajasthan welcomes royal tourists and backpacking travellers equally. Your choices range from luxurious hotels to economical yet comfortable dormitories. The State owned Rajasthan Tourism Development Corporation also run a chain of hotels in the major destinations. These tourist bungalows provide reasonably priced accommodation in the form of dormitories, single rooms, air-cooled and air-conditioned rooms.</p>
